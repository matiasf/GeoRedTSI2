package com.geored.rest.data;


public class Usuario {
	
	String nombre;
	String id;

	public Usuario() {	}
	
	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

}
